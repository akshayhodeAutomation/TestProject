package javaVariables;

public class staticVariable {
	
	
	public static void main(String[] args) {
		
		staticVariable s=new staticVariable();
		s.test();
		
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(a+b);

	
	}
	
	
	static int a=25;
	static int b=25;
	
	public void test()
	{
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(a+b);
		
	}
	
	
	

}
