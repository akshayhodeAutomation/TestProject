package javaAbstractionClass;

abstract class abstractClass {
	
	abstract void classA();
	abstract void classB();
	abstract void classC();
	
	
	static void std()
	{
		System.out.println("10th standard");
	}

}



abstract class CityAnimal
{
	
	static void cityAnimals()
	{
		System.out.println("City Animals");
	}
	
	abstract void dog();
	abstract void cat();
	
}
	



abstract class WildAnimals
{
	static void WildAnimals()
	{
		System.out.println("Wild Animals");
	}
	
	abstract void lion();
	abstract void tiger();
	abstract void elephant();

}
	
	
